function parseTime(timeStr: string): number {
  if (!timeStr || timeStr === '') return 0;
  const parts = timeStr.split(':');
  if (parts.length === 3) {
    return parseInt(parts[0]) * 3600 + parseInt(parts[1]) * 60 + parseInt(parts[2]);
  } else if (parts.length === 2) {
    return parseInt(parts[0]) * 60 + parseInt(parts[1]);
  }
  return 0;
}

function parseDate(dateStr: string): Date {
  try {
    return new Date(dateStr);
  } catch {
    return new Date();
  }
}

function parseCSVLine(line: string): string[] {
  const result: string[] = [];
  let current = '';
  let inQuotes = false;
  
  for (let i = 0; i < line.length; i++) {
    const char = line[i];
    if (char === '"') {
      if (inQuotes && line[i + 1] === '"') {
        current += '"';
        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (char === ',' && !inQuotes) {
      result.push(current.trim());
      current = '';
    } else {
      current += char;
    }
  }
  result.push(current.trim());
  return result;
}

let _cachedVideos: VideoData[] | null = null;

export function parseVideoData(): VideoData[] {
  if (_cachedVideos) return _cachedVideos;
  
  const lines = RAW_CSV.split('\n').slice(1); // skip header
  const videos: VideoData[] = [];
  
  for (const line of lines) {
    if (!line.trim()) continue;
    
    const cols = parseCSVLine(line);
    if (cols.length < 12) continue;
    
    const id = cols[0]?.trim();
    const title = cols[1]?.trim();
    const publishedAt = cols[2]?.trim();
    const duration = parseInt(cols[3]) || 0;
    const likeRate = parseFloat(cols[4]) || 0;
    const avgViewDuration = cols[5]?.trim() || '0:00:00';
    const views = parseInt(cols[6]) || 0;
    const totalWatchHours = parseFloat(cols[7]) || 0;
    const subscribers = parseInt(cols[8]) || 0;
    const estimatedRevenue = parseFloat(cols[9]) || 0;
    const impressions = parseInt(cols[10]) || 0;
    const ctr = parseFloat(cols[11]) || 0;
    
    if (!id || !title) continue;
    
    videos.push({
      id,
      title,
      publishedAt,
      duration,
      likeRate,
      avgViewDuration,
      avgViewDurationSeconds: parseTime(avgViewDuration),
      views,
      totalWatchHours,
      subscribers,
      estimatedRevenue,
      impressions,
      ctr,
      publishedDate: parseDate(publishedAt),
    });
  }
  
  _cachedVideos = videos;
  return videos;
}

export function getChannelSummary() {
  const videos = parseVideoData();
  const validRevenue = videos.filter(v => v.estimatedRevenue > 0);
  
  return {
    totalViews: videos.reduce((s, v) => s + v.views, 0),
    totalWatchHours: videos.reduce((s, v) => s + v.totalWatchHours, 0),
    totalRevenue: validRevenue.reduce((s, v) => s + v.estimatedRevenue, 0),
    totalImpressions: videos.reduce((s, v) => s + v.impressions, 0),
    avgCtr: videos.reduce((s, v) => s + v.ctr, 0) / videos.length,
    avgLikeRate: videos.reduce((s, v) => s + v.likeRate, 0) / videos.length,
    videoCount: videos.length,
  };
}

export function getMonthlyStats() {
  const videos = parseVideoData();
  const monthMap = new Map<string, { views: number; revenue: number; videoCount: number; watchHours: number }>();
  
  for (const video of videos) {
    const date = video.publishedDate;
    if (isNaN(date.getTime())) continue;
    const month = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;
    
    const existing = monthMap.get(month) || { views: 0, revenue: 0, videoCount: 0, watchHours: 0 };
    existing.views += video.views;
    existing.revenue += video.estimatedRevenue > 0 ? video.estimatedRevenue : 0;
    existing.videoCount += 1;
    existing.watchHours += video.totalWatchHours;
    monthMap.set(month, existing);
  }
  
  return Array.from(monthMap.entries())
    .map(([month, stats]) => ({ month, ...stats }))
    .sort((a, b) => a.month.localeCompare(b.month));
}

export function getTopVideos(type: 'views' | 'revenue' | 'watchHours' | 'ctr' | 'likeRate', limit = 20, period?: string) {
  let videos = parseVideoData();
  
  if (period && period !== 'all') {
    const now = new Date();
    const months = period === '3months' ? 3 : period === '6months' ? 6 : 12;
    const cutoff = new Date(now.getFullYear(), now.getMonth() - months, now.getDate());
    videos = videos.filter(v => v.publishedDate >= cutoff);
  }
  
  const sorted = [...videos].sort((a, b) => {
    switch (type) {
      case 'views': return b.views - a.views;
      case 'revenue': return b.estimatedRevenue - a.estimatedRevenue;
      case 'watchHours': return b.totalWatchHours - a.totalWatchHours;
      case 'ctr': return b.ctr - a.ctr;
      case 'likeRate': return b.likeRate - a.likeRate;
      default: return b.views - a.views;
    }
  });
  
  return sorted.slice(0, limit);
}

export function calculatePerformanceScore(video: VideoData): { score: number; grade: 'S' | 'A' | 'B' | 'C' | 'D' } {
  const allVideos = parseVideoData();
  const avgViews = allVideos.reduce((s, v) => s + v.views, 0) / allVideos.length;
  const avgRevenue = allVideos.filter(v => v.estimatedRevenue > 0).reduce((s, v) => s + v.estimatedRevenue, 0) / allVideos.length;
  const avgCtr = allVideos.reduce((s, v) => s + v.ctr, 0) / allVideos.length;
  const avgLikeRate = allVideos.reduce((s, v) => s + v.likeRate, 0) / allVideos.length;
  
  const viewsScore = Math.min(100, (video.views / avgViews) * 50);
  const revenueScore = video.estimatedRevenue > 0 ? Math.min(100, (video.estimatedRevenue / avgRevenue) * 25) : 0;
  const ctrScore = Math.min(100, (video.ctr / avgCtr) * 15);
  const likeScore = Math.min(100, (video.likeRate / avgLikeRate) * 10);
  
  const total = viewsScore + revenueScore + ctrScore + likeScore;
  const score = Math.round(Math.min(100, total));
  
  let grade: 'S' | 'A' | 'B' | 'C' | 'D';
  if (score >= 80) grade = 'S';
  else if (score >= 60) grade = 'A';
  else if (score >= 40) grade = 'B';
  else if (score >= 20) grade = 'C';
  else grade = 'D';
  
  return { score, grade };
}

export function formatNumber(n: number): string {
  if (n >= 100000000) return `${(n / 100000000).toFixed(1)}億`;
  if (n >= 10000) return `${(n / 10000).toFixed(1)}万`;
  if (n >= 1000) return `${(n / 1000).toFixed(1)}千`;
  return n.toLocaleString();
}

export function formatRevenue(n: number): string {
  if (n >= 100000000) return `¥${(n / 100000000).toFixed(1)}億`;
  if (n >= 10000) return `¥${(n / 10000).toFixed(0)}万`;
  return `¥${n.toLocaleString()}`;
}

export function formatDuration(seconds: number): string {
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = seconds % 60;
  if (h > 0) return `${h}:${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}`;
  return `${m}:${String(s).padStart(2, '0')}`;
}
